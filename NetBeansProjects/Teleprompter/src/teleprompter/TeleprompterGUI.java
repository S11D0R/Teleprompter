    private int CaretPosition(){
        if (jTextArea2.getCaretPosition() + 1 < jTextArea2.getDocument().getLength())
            return jTextArea2.getCaretPosition() + 10;
        else
            return jTextArea2.getDocument().getLength();
        
    }
